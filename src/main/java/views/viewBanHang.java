    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int TongTien = Integer.parseInt(txtThanhTien.getText());
        int KhachTra = Integer.parseInt(txtKhachTra.getText());

        if (txtKhachTra.getText().isEmpty()) {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/TraTien.png"));
            JOptionPane.showMessageDialog(this, "Chua nhập tiền khách trả", "Horrry !", JOptionPane.INFORMATION_MESSAGE, icon);
            return;
        }
        if (txtNgay.getText().isEmpty()) {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/TraTien.png"));
            JOptionPane.showMessageDialog(this, "Chua nhập Ngày Thanh Toán", "Horrry !", JOptionPane.INFORMATION_MESSAGE, icon);
            return;
        }
        if (KhachTra < TongTien) {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/TraTien.png"));
            JOptionPane.showMessageDialog(this, "Chưa Thanh Toán Đầy Đủ", "Horrry !", JOptionPane.INFORMATION_MESSAGE, icon);
            return;
        } else {
            serviceHd.update(txtIdHD.getText(), txtNgay.getText());
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/hoadonicon.png"));
            JOptionPane.showMessageDialog(this, "Thanh Toán Thành Công", "Horrry !", JOptionPane.INFORMATION_MESSAGE, icon);
            loadHd();
            txtIdHD.setText("");
            txtMaHd.setText("");
            txtNgay.setText("");
            txtThanhTien.setText("");
            txtKhachTra.setText("");
            txtTienThua.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHDMouseClicked

        int index = tblHD.getSelectedRow();
        String idhd = tblHD.getValueAt(index, 1).toString();
        String maHd = tblHD.getValueAt(index, 2).toString();
        String TrangThai = tblHD.getValueAt(index, 3).toString();
        Object day = tblHD.getValueAt(index, 4);
        if (day == null) {
            txtNgay.setText("");
        } else {
            String daytt = tblHD.getValueAt(index, 4).toString();
            txtNgay.setText(daytt);
        }
        txtIdHD.setText(idhd);
        txtMaHd.setText(maHd);
        if (TrangThai.equals("Đã Thanh Toán")) {
            txtIdHD.setEditable(false);
            txtMaHd.setEditable(false);
            tblSanPham.setEnabled(false);
            tblGioHang.setEnabled(false);
            txtKhachTra.setEditable(false);
            txtThanhTien.setEditable(false);
            txtTienThua.setEditable(false);
            txtNgay.setEditable(false);

        } else {
            txtIdHD.setEditable(true);
            txtMaHd.setEditable(true);
            tblSanPham.setEnabled(true);
            tblGioHang.setEnabled(true);
            txtKhachTra.setEditable(true);
            txtThanhTien.setEditable(true);
            txtTienThua.setEditable(true);
            txtNgay.setEditable(true);
        }
        loadgh();
        int tongtien = serviceHd.tongTien(txtIdHD.getText());
        txtThanhTien.setText(String.valueOf(tongtien));
    }//GEN-LAST:event_tblHDMouseClicked

