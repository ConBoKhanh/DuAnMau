    private void tblGioHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGioHangMouseClicked
        int index = tblGioHang.getSelectedRow();
        String idhd = tblGioHang.getValueAt(index, 1).toString();
        String idsp = tblGioHang.getValueAt(index, 2).toString();
        // load list cua button
        String[] buttons = {"Update", "Delete", "Cancel"};

        int rc = JOptionPane.showOptionDialog(null, "Question ?", "Confirmation",
                JOptionPane.WARNING_MESSAGE, 0, null, buttons, buttons[2]);

        if (rc == 0) {// update lai so luong
            String soLuongNhapinpit = JOptionPane.showInputDialog("Nhập Số Lượng Update của Sản Phẩm Sản Phẩm ");
            // NHẬP SỐ LƯỢNG ĐỂ INSERT 
            if (soLuongNhapinpit.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui Lòng Nhập");
                return;
            }

            int SoLuongNhap = Integer.parseInt(soLuongNhapinpit);

            // update
            boolean b = servicegh.update(idhd, idsp, SoLuongNhap);
            if (b == true) {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/clear.png"));
                JOptionPane.showMessageDialog(this, "Update Sản Phẩm Thành Công", "Giỏ Hàng", JOptionPane.INFORMATION_MESSAGE, icon);
                loadgh();
                int tongtien = serviceHd.tongTien(txtIdHD.getText());
                txtThanhTien.setText(String.valueOf(tongtien));
            } else {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/deleteicon.png"));
                JOptionPane.showMessageDialog(this, "Update Sản Phẩm Thất Bại", "Giỏ Hàng !", JOptionPane.INFORMATION_MESSAGE, icon);
            }

            return;
        }
        if (rc == 1) {
            boolean b = servicegh.delete(idhd, idsp);
            if (b == true) {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/clear.png"));
                JOptionPane.showMessageDialog(this, "Delete Sản Phẩm Thành Công", "Giỏ Hàng", JOptionPane.INFORMATION_MESSAGE, icon);
                loadgh();
                int tongtien = serviceHd.tongTien(txtIdHD.getText());
                txtThanhTien.setText(String.valueOf(tongtien));
            } else {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/deleteicon.png"));
                JOptionPane.showMessageDialog(this, "Delete Sản Phẩm Thất Bại", "Giỏ Hàng !", JOptionPane.INFORMATION_MESSAGE, icon);
            }
            return;
        }
        if (rc == 2) {
            return;
        }


    }//GEN-LAST:event_tblGioHangMouseClicked

