    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int index = tblSanPham.getSelectedRow();

        String Tensp = tblSanPham.getValueAt(index, 3).toString();
        int soluong = Integer.parseInt(tblSanPham.getValueAt(index, 7).toString());
        if (index < 0) {
            return;
        }
        String soLuongNhapinpit = JOptionPane.showInputDialog("Nhập Số Lượng Sản Phẩm " + " " + Tensp);
        // NHẬP SỐ LƯỢNG ĐỂ INSERT 
        if (soLuongNhapinpit.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập");
            return;
        }

        String DonGiast = tblSanPham.getValueAt(index, 9).toString();
        //
        String idsp = tblSanPham.getValueAt(index, 1).toString();
        int DonGia = Integer.parseInt(DonGiast);
        String idhd = txtIdHD.getText();
        if (idhd.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa chọn Hóa Đơn");
            return;
        }
        int SoLuongNhap = Integer.parseInt(soLuongNhapinpit);
        int soluongconlai = soluong - SoLuongNhap;

        boolean b = servicegh.add(idhd, idsp, SoLuongNhap, DonGia);
        if (b == true) {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/themmoiicon.png"));
            JOptionPane.showMessageDialog(this, "Thêm Sản Phẩm Thành Công", "Giỏ Hàng", JOptionPane.INFORMATION_MESSAGE, icon);
            service.update(idsp, soluongconlai);
            loadgh();
            loadtSp();
            int tongtien = serviceHd.tongTien(txtIdHD.getText());
            txtThanhTien.setText(String.valueOf(tongtien));
        } else {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/deleteicon.png"));
            JOptionPane.showMessageDialog(this, "Sản Phẩm Đã Tồn Tại", "Giỏ Hàng !", JOptionPane.INFORMATION_MESSAGE, icon);
        }


    }//GEN-LAST:event_tblSanPhamMouseClicked

