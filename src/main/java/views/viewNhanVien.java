    public void loadNV() {
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        List<NhanVienViewModel> list = s.getNṾ();
        for (NhanVienViewModel a : list) {
            model.addRow(new Object[]{
                a.getId(), a.getMa(), a.getTen(), a.getGioiTinh(), a.getSdt(),
                a.getPass(), a.getChucVu(), a.getCuaHang(), a.getTrangThai() == 0 ? "Off" : "On"

            });
        }
    }

    public void loadCV() {
        model = (DefaultTableModel) tblCV.getModel();
        model.setRowCount(0);
        List<ChucVuModelView> list = cv.getList();
        for (ChucVuModelView a : list) {
            model.addRow(new Object[]{
                a.getId(), a.getMa(), a.getTen()
            });
        }

    }

    public String getCV(String Tencv) {
        String id = null;
        List<ChucVuModelView> list = cv.getList();
        for (ChucVuModelView a : list) {
            if (a.getTen().equals(Tencv)) {
                id = a.getId();
            }
        }
        return id;
    }

    public String getCH(String Tench) {
        String id = null;
        List<CuaHangViewModel> list = ch.getlist();
        for (CuaHangViewModel a : list) {
            if (a.getTen().equals(Tench)) {
                id = a.getId();
            }
        }
        return id;
    }

    public void loadcbc() {
        cbcCH.removeAllItems();
        cbCV.removeAllItems();
        List<ChucVuModelView> list = cv.getList();
        for (ChucVuModelView b : list) {
            cbCV.addItem(b.getTen());
        }

        List<CuaHangViewModel> list1 = ch.getlist();
        for (CuaHangViewModel b : list1) {
            cbcCH.addItem(b.getTen());
        }

    }

    public void loadCH() {
        model = (DefaultTableModel) tblCH.getModel();
        model.setRowCount(0);
        List<CuaHangViewModel> list = ch.getlist();
        for (CuaHangViewModel a : list) {
            model.addRow(new Object[]{
                a.getId(), a.getMa(), a.getTen(), a.getDiaChi(), a.getThanhPho(), a.getQuocGia()
            });
        }

    }

