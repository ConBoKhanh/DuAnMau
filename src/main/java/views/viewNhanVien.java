    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NhanVien nv = new NhanVien();
        nv.setMa(txtMa.getText());
        nv.setTen(txtTen.getText());
        if (rdoNam.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }
        nv.setSdt(txtSdt.getText());
        nv.setMatKhau(txtpass.getText());
        nv.setTrangThai(1);
        String idch = getCH((String) cbcCH.getSelectedItem());
        String idcv = getCV((String) cbCV.getSelectedItem());

        boolean e = s.add(nv, idch, idcv);
        if (e == true) {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/themmoiicon.png"));
            JOptionPane.showMessageDialog(this, "Thêm Thành Công", "Nhân Viên", JOptionPane.INFORMATION_MESSAGE, icon);
            loadNV();
        } else {
            Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img_icon/deleteicon.png"));
            JOptionPane.showMessageDialog(this, "Mã Trùng", "Nhân Viên !", JOptionPane.INFORMATION_MESSAGE, icon);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

