    public void loadHd() {

        List<HoaDonViewModel> list = serviceHd.getHd();

        model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        for (HoaDonViewModel o : list) {
            model.addRow(new Object[]{
                o.getIdHD(),
                "Hóa Đơn " + o.getMa(),
                o.getTrangThai() == 0 ? "Chưa Thanh Toán" : "Đã Thanh Toán",
                o.getNgaytt(),
                o.getTenNV()

            });
        }
    }

    public void loadgh() {

        List<HoaDonChiTietViewModel> list = servicegh.getListSP(lbId.getText());

        model = (DefaultTableModel) tblHoaDonChiTiet.getModel();
        model.setRowCount(0);
        for (HoaDonChiTietViewModel o : list) {
            model.addRow(new Object[]{
                o.getTensp(), o.getSoLuong(), o.getDonGia()

            });
        }
    }

